    cmd = {
      "NvimChangelog",
      "NvimFreezePluginVersions",
      "NvimReload",
      "NvimRollbackCreate",
      "NvimRollbackRestore",
      "NvimUpdateConfig",
      "NvimUpdatePlugins",
      "NvimVersions"
    },
  }
